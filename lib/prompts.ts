/**
 * Формирует пользовательский промпт с контекстом об игроке и матче.
 */
export function buildUserPrompt(context: {
  playerName: string;
  playerAge: number | null;
  playerPosition: string | null;
  opponent: string | null;
  matchType: string | null;
}): string {
  const parts: string[] = [
    `Проанализируй игру футболиста по предоставленному видео/изображениям.`,
    `Имя игрока: ${context.playerName}`,
  ];

  if (context.playerAge) {
    parts.push(`Возраст: ${context.playerAge} лет`);
  }
  if (context.playerPosition) {
    parts.push(`Позиция: ${context.playerPosition}`);
  }
  if (context.opponent) {
    parts.push(`Соперник: ${context.opponent}`);
  }
  if (context.matchType) {
    parts.push(`Тип матча: ${context.matchType}`);
  }

  parts.push(
    `\nДай детальный анализ и составь элитный план тренировок строго в формате JSON, как указано выше. Не добавляй никакого текста за пределами JSON.`
  );

  return parts.join('\n');
}
